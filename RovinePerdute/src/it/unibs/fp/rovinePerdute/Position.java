package it.unibs.fp.rovinePerdute;

/**
 * Class to implements in the City object for the position. Cartesian position X and Y
 * and H for altitude.
 */
public class Position {

	private Integer X, Y, H;

	//Getter
	public Integer getX() {
		return X;
	}

	public Integer getY() {
		return Y;
	}

	public Integer getH() {
		return H;
	}

	//Setter
	public void setX(Integer x) {
		X = x;
	}

	public void setY(Integer y) {
		Y = y;
	}

	public void setH(Integer h) {
		H = h;
	}

}
